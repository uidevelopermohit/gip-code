                            <h5>Progress and Challenges of Green Finance in India, Reserve Bank of India</h5>
                           
                            
                            <p>
                                India has started emphasising on green finance as early as 2007. In December 2007, the Reserve Bank issued a notification on “Corporate Social Responsibility, Sustainable Development and Non-financial Reporting – Role of Banks” and mentions the importance of global warming and climate change in the context of sustainable development. In 2008, The National Action Plan on Climate Change (NAPCC) was formulated with a vision to outline the broad policy framework for mitigating the impact of climate change (Jain, 2020). The Climate Change Finance Unit (CCFU)was formed in 2011 within the Ministry of Finance as a coordinating agency for the various institutions responsible for green finance in India. The major strategic move since 2012 included implementation of the sustainability disclosure requirements. Security and Exchange Board of India (SEBI) made it mandatory for top 100 listed entities based on market capitalisation at BSE and NSE to publish8 Future of Finance, (2019), Bank of England, June. annual business responsibility reports since 2012 and revised it from time to time. In May 2017, SEBI issued guidelines for green bond issuance specifying the disclosure requirements. In addition, the Ministry of Corporate Affairs imposed mandatory reporting of the progress on Corporate Social Responsibilities (CSR) under the Companies Act, 2013.9 In October 2017, Report of the Committee on Corporate Governance has proposed that the board of directors shall meet at least once a year to specifically discuss strategy, budgets, board evaluation, risk management, ESG and succession planning. The Reserve Bank is sensitising public, investors and banks regarding the need, opportunities, and challenges of green finance through its regular reports and other communications. 
                            </p>

                            <p>
                                <b>Borrowing costs </b><br>
                                The cost of issuing green bonds has generally remained higher than the other bonds in India. Panel a in chart 4 shows that the average coupon rate for green bonds issued since 2015 with maturities between 5 to 10 years have generally remained higher than the corporate and government bonds with similar tenure. Panel b in chart 4 shows a similar pattern for the INR denominated green bonds. For the US$ denominated green bonds with tenure of more than or equal to 10 years, the coupon rate was, however lower than the corporate bonds. Existing literature suggests that green projects often have a high up-front cost with some cost-saving features only applicable in the long-term. Maturity-mismatches in green projects and their financing increase the cost of borrowing (G20 GFSG, 2016). 
                            </p>
                          
                            
                            <p>
                                <b>Borrowing cost and information asymmetry </b>
                                High borrowing cost has been perhaps the most important challenge, and our analysis indicates that it could be due to the asymmetric information. Therefore, developing a better information management system in India may help in reducing maturity mismatches, borrowing costs and lead to efficient resource allocation in this segment. It may be mentioned that to overcome such information gap, several countries including Australia, China, India and the United States have database related to green building projects in the country (Shen et al 2020). While India does monitor greenhouse gas emissions through various reporting mechanisms including PAT (perform-achieve trade) and RPO (renewable purchase obligations), like many other countries it does not have a national measurement, reporting and verification platform for tracking climate finance (Jan, 2020).  
                            </p>

                            <p>
                                <b>Market infrastructure development</b><br> 
                                Given the large size of domestic market and much smaller penetration of green instruments so far, there remain vast opportunities to be tapped. In this context, some of the studies noted the importance of (a) increased coordination between investment and environmental policies and (b) an implementable policy framework for both national and state levels in addressing the existing frictions. In this vein, some of the policy measures such as deepening of corporate bond market, standardisation of green investment terminology, consistent corporate reporting, and removing information asymmetry between investors and recipients can make a significant contribution in addressing some of the shortcomings of the green finance market (RBI, 2019).

                            </p>
