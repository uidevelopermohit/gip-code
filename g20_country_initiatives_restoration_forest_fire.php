                            <h5>Restoration of Forest Fire Impacted areas in Bandipur Tiger Reserve, Karnataka</h5>
                            <p>
                                <b>Brief Summary: </b>
                                    At Bandipur Tiger reserve, on a pilot basis, Eco-restoration of Forest fire affected areas were 
                                    undertaken after 2019 major fire incident in GS Betta Range. Efforts were being made to restore 340 
                                    ha out of 2150 ha of burnt area.
                                </p>
                                <p>
                                    Forest fire in the various beats of GS Betta Range complete burning of Ground cover, herbs and 
                                    majority of shrubs and bamboo which resulted in destruction of saplings, seeds in the soil bed and 
                                    suppression of native floral species, but fire loving invasive weed species like Lantana camara and 
                                    Senna spectabilis flourished without any competition. Abundant growth of Lantana camara and Senna 
                                    spectabilis made the habitat impenetrable for other native grass, herbs and shrubs species resulted 
                                    in poor biodiversity.
                                </p>
                                <p>
                                    A combination of these factors has led to most of the fire-affected plots having high L. camara 
                                    density within 12-18 months after the fire.
                                </p>
                                <p>
                                    <b>Lessons Learned:</b> 
                                    In the restored plots, the diversity of adult trees is moderate with around 65 species observed. 
                                    However, abundance is low in the case of almost all species. This is typical of L. camara invaded 
                                    areas due to suppression of recruitment over a long period.
                                </p>
                                <p>
                                    Good recruitment of over 60 tree species is observed, with presence of seedlings and juvenile 
                                    plants. This includes many important species like Acacia chundra, Acacia sinuata, Aegis marmelos, 
                                    Albizia lebbeck, Bauhinia racemosa, Boswellia serrata, Butea monosperma, Diospyros montana, Grewia 
                                    tillifolia, Dalbergia lanceolaria, Ixora pavetta, Limonia monophylla, Naringi crenulata, Pterocarpus 
                                    marsupium, and Santalum album etc. 
                                </p>
                                <p>
                                     More natural recruitment progression is expected after the removal of L. camara, with more species 
                                     appearing in due course through propagules from in-situ adult trees as well as seed migration from 
                                     other areas aided by dispersal agents like elephants. These species are present in sapling 
                                     condition only and there were very few big trees due to the major fire. In due course of time, 
                                     if the habitat is protected from forest fires, it can be transformed into a dense woodland.
                                </p>
                                <p>
                                    There is good diversity and abundance of under-storey plant habits of shrub, herb, climber etc. 
                                    post removal of L. camara, with over 200 species observed. This is typical response to removal of 
                                    invasive species, with the release of space, sunlight and nutrients leading to a “recruitment 
                                    euphoria”, particularly of fast-establishing plant habits like herbs and shrubs. The re-emergence is 
                                    facilitated by both in-situ propagules and seed migration. While many shrub species are perennial, a 
                                    majority of the herb species are seasonal. There is a good re-emergence of Dendrocalamus strictus in 
                                    some of the restored plots. However, the presence of Bambusa bambos is low.
                                </p>
                                <p>
                                    A total of 355 plant species were observed in the restored plots. Around 22 endemic species and 6 
                                    threatened species were also observed. Good presence of plants foraged upon by elephants and other 
                                    herbivores is also observed.
                                </p>
