                                <h5>Community Involvement and Engagement in Forest Fire Management</h5>
                            </div>
                            <div class="col_12"> 
                                <p>
                                There are various communities living inside and very close to the forests in India, the sustenance of these communities is intertwined with the forest ecosystems in more than one way. The dependency of general public on the forests ranges from availing the food from themselves to fodder for the cattle, forests also provide resources for housing etc. and a variety of minor products, hence, these communities would the first to bear the brunt of any damage caused to the forests. (Banerjee and Madhurima, 2013) which is why it seems crucial to involve these dependent communities in formulating any policies or plans to protect or restore the landscapes.
                                </p>
                                
                               
                            </div>
                        </div>
                        <div class="flex_row">
                            <div class="col_12"> 
                                <p>
                                Evidence from the most forest dense districts of Orissa (an Indian state) suggest that the initiatives that these local communities take to prevent or manage the incidences of forest fires is closely related to how much the dependency is, if a scarcity of resources occur, they are more likely to take quick steps for the control. There were activities carried out by locals which had the potential to cause damage, but the provisions taken up showed that the community had potential to keep fire in control (Nnada and Sutar,2021), which serves as an example of capacity building among the locals themselves.

                                </p>
                                <p>
                                  Community based fire management techniques were employed in Uttar Kannada district of Karnataka state of India, the measures taken were a mixture of community involvement and capacity building approaches such as awareness programmes, street plays, door to door campaigns, fire control rooms, building of watch towers etc. was done. The forest department started receiving more than 5% less alerts only within a year of the programme, the burnt area as a result of forest fires in over period of one year, drastically decreased by more than 58%. This served as an evidence for how dramatically better reporting, putting out, and managing forest fires have been made possible by community-based techniques. (Prashanth et. al, 2020)
                                  
