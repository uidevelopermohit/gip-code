                            <p>
                                The overall impact of large scale deforestation has been very devastating, while on one hand the indigenous people in the tropical forests have faced poverty and alienation due to diminishing supplies of forest products and farm yields, on the other; it caused immense loss to environment and ecology. There have been profound ecological effects of forest loss, as evident in the exacerbation of droughts and floods, release of heat trapping temperatures, advent of new pests into cropped lands, much sedimentation in riverbeds and hydroelectric reservoirs, and loss of productive fisheries. 
                                More than 90 percent forest fires all over the world are initiated due to anthropogenic activities, which majority is related to land use pattern. Conversion of forestland to agriculture, pastureland and plantation are the main eminent factors for such incidences. In tropics, two types of practices for conversion of forest land are common i.e. shifting cultivation and permanent conversion.

                                </p>
