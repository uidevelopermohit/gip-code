                            <h5>Managing Forest Fires in a Changing Climate: Council on Energy Environment and Water (CEEW)</h5>
                            <p>
                                The study argues that a comprehensive assessment for forest fires by integrating climatological scenarios at a localised level can help mitigate forest fires better. Our analysis suggests that there has been a 10-fold increase in forest fire incidences in the last two decades, whereas the total forest cover (TFC) has only increased by 1.12 per cent. Our analysis further suggests that tropical moist deciduous forests, followed by tropical dry deciduous forests, are most vulnerable to forest fires.
                                </p>
                                <p>
                                <b>CEEW’s recommendations to climate-proof forests are as follows:</b>
                                </p>
                                <p>
                                    <b>1. Recognise forest fires as a disaster type and integrate them into national, sub-national and local disaster management plans:</b> The National Plan on Forest Fires was launched by the Government of India in 2018 under the flagship National Afforestation Programme. This plan touches upon a wide range of activities, but financial constraints and operational dynamism remains a challenge. Forest fires should be recognised as a disaster type under the NDMA act. The recognition will enhance and strengthen the National Plan on Forest Fires by improving its financial allocation and through the creation of a cadre of trained forest firefighters under the National Disaster Response Force (NDRF) and State Disaster Response Force (SDRF). 
                                </p>
                                <p>
                                    <b>2. Develop a forest fire-only alert system:</b> Currently, Forest Survey of India (FSI) and National Remote Sensing Centre (NRSC) use MODIS and Suomi NPP VIIRS information for near real-time monitoring of forest fires which does not segregate forest fires from other fires such as waste burning and crop burning. Hence, ground validation takes time and the scope for misinformation increases. As a first step, a forest fire ONLY alert system needs to be developed that can provide real-time impact-based alerts. 
                                </p>
                                <p>
                                    <b>3. Enhance adaptive capacity:</b> Capacity-building initiatives targeted at district administrations and forest-dependent communities can avert the extent of loss and damage due to forest fires. Training on high technology-focused equipment (like drones) and nature-based modules such as creating effective forest fire lines can effectively mitigate the spread of forest fires.  
                                </p>
                                <p>
                                    <b>4. Provide clean air shelters: </b>The state government/ state forest departments (SFDs) should repurpose public buildings like government schools and community halls by fitting them with clean air solutions – like air filters – to create clean air shelters for communities worst impacted by fires and smoke from forest fires.
                                </p>
                                
