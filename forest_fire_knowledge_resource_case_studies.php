                                <h5>(b) Forest Fire Management: Global Best Practices, National Disaster Management Authority, India:</h5>
                                        <p>
                                            With climate change leading to drying of biomass and adding organic build-up over time the 
                                            forest fires are enormous and are no longer beneficial to the forest health. According to the 
                                            Fifth Assessment Report of the Intergovernmental Panel on Climate Change, annual carbon 
                                            emissions from forest fires are in the range of 2.5 billion to 4.0 billion tons of CO2 (MoEFCC, 
                                            2018). Globally, the raging forest fires are affecting the wildlife habitats and biodiversity. 
                                            These fires are also displacing millions of people and taking away livelihoods. 
                                        </p>
                                        <p>
                                                A forest fire can be divided into three phases- Grass, undergrowth and crown fires. 
                                                Usually, grass and the dry undergrowth ignite first and are easy to control. If the 
                                                fire reaches the treetops, especially in the case of conifers, leading to a crown or 
                                                canopy fire it becomes considerably more difficult to control.
                                        </p>
                                        <p>
                                                Globally, only around 4% of all forest fires have natural causes such as lightning. 
                                                In all other cases, the anthropogenic origin of the fires is common. Often, the burned 
                                                areas and with it the entire ecosystem with the plants and animals living within it, are 
                                                irretrievably lost. During the last decades, both distribution and frequency of 
                                                wildfires have been changing in many areas across the world, due to changes in climate, 
                                                landscapes, or socioeconomic drivers. These drivers often work hand in hand. This is 
                                                particularly important in areas where humans are the major source of ignitions and 
                                                landscape change. 
                                        </p>
                                        <p>
                                                Nonetheless, the effects of those driving factors on wildfires vary across temporal and 
                                                spatial scales, requiring spatio-temporal models capable of simulating the spatial and 
                                                temporal fire patterns. Forest fire is a growing threat to communities around the world. 
                                                Traditionally, reducing forest fire risk was co-managed by the administration and local 
                                                communities. But over time the local communities have been sidelined to pave way for 
                                                forest departments/ fire departments to manage the forest fire. However, community 
                                                driven forest fire management can reduce the deaths of residents. The loss of property 
                                                and damages to infrastructure can be hugely reduced, thus saving millions of taxpayers’ 
                                                money.
                                        </p>
