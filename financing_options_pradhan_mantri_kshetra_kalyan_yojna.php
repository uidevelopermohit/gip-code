                           <p>
                                <b>PMKKKY provides for utilization of at least 60% of the funds for high priority areas like: </b>
                           </p>


                           <p>
                                (i) drinking water supply; (ii) environment preservation and pollution control measures; (iii) health care; (iv) education; (v) welfare of women and children; (vi) welfare of aged and disabled people; (vii) skill development; and (viii) sanitation. While, up to 40% of the funds shall be utilized for other priority areas (i) physical infrastructure; (ii) irrigation; (iii) energy and watershed development; and (iv) any other measures for enhancing environmental quality in mining district.
                                    
                           </p>
                           <p><b>General guidelines</b></p>
                           <p>
                                a. The developmental and welfare activities to be taken up under the PMKKKY should be, as far as possible, in the nature of complementing the ongoing schemes/projects being funded by the State as well Central Government. Activities meant to be taken up under the ‘polluter pays principle’ should not be taken up under the PMKKKY. However, without prejudice to the powers of the Foundation, efforts shall be made to achieve convergence with the State and the District Plans so that the activities taken up by the Foundation supplement the development and welfare activities and are treated as extrabudgetary resources for the State Plan.
                           </p>
                           <p>
                                b. An amount not exceeding 5% of the annual receipts of the Foundation subject to an upper limit fixed by state government may be utilised for administrative, supervisory and overhead costs of the Foundation. As far as possible, no temporary/permanent posts should be created under PMKKKY. Any creation of temporary/permanent posts and purchase of vehicle by the foundation shall require prior approval of the State Government. However, minimum required staff can be engaged on contractual basis.
                           </p>
                           <p>
                                c. If the affected area of a mine in one district also falls in the jurisdiction of another district, such percentage of amount collected from the mine by the Foundation, as may be decided by the Government, shall be transferred to the Foundation of the other district concerned for taking up the activities in such areas. A project that is for benefit of the affected area/ people but stretches beyond the geographical boundary of the district should be taken up under the PMKKKY after obtaining prior approval of the State Government. Projects for development of common infrastructure like construction of roads, bridges etc. in excess of the limits specified in regard to the priority for fund utilization, on a case to case basis, may also be taken up for projects of importance to the District. The prior approval of the State Government need to be taken, with intimation to the Central Government, before taking up such works in excess of the limits of fund utilization.
                           </p>
                           <p>
                                d. A reasonable sum of the annual receipts should be kept as endowment fund for providing sustainable livelihood.
                           </p>

