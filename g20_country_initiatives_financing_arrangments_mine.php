                                    Plans by Govt. of Karnataka on directions of the Central Empowered Committee (CEC) of Hon able 
                                    Supreme Court of India. Reclamation and Rehabilitation Plans for 129 iron ore mine leases of 
                                    different category A, B & C covering a total mine lease area of 9793.511 ha has been so far 
                                    approved by the CEC for implementation by respective lessee in the state of Karnataka. The 
                                    overall proposed cost is around Rs. 35,587 Lakh and the average rate is Rs 3.63 lakhs/ha. However, 
                                    R&R cost for each mine is variable subject to mine lease area, reserves, OB dump capacity and road 
                                    capacity, topography, illegal mining due to encroachment, if any and damage caused to the 
                                    environment etc. The R&R plans mainly includes surface water management plan and dump management 
                                    plan for different engineering measures like construction of toe wall, retaining wall, benches, top 
                                    leveling and surface water management measures like catch drains, garland drains, check dams, silt 
                                    settling tanks along with the biological measures by suggesting plant species according to the 
                                    plantation site such as on refractory sites, dump, avenue, greenbelt, safety zone etc. The 
                                    Monitoring Committee, Dept. of Mines and Geology, Karnataka monitors implementation of the 
                                    activity by the lessee on ground suggested by ICFRE in R&R Plan.
