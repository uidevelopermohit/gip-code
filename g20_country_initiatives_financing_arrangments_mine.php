                            <h5>Financing Arrangements for Mine Restoration and Rehabilitation </h5>
                                <p>
                                   There is hardly any government, agencies or major funds which provide direct financing to mining 
                                   reclamation projects especially for abandoned /orphaned mines prior to the promulgation of rules 
                                   about Mine Closure Plan in April 2003. However, public sector banks, in addition to private 
                                   commercial banks and non-banking financial companies, provide debt financing for mining projects 
                                   including reclamation activities in India. The mining related statue has a provision of preparing 
                                   Progressive Mine Closure Plan which is linked with the collection of financial assurance, reviewed 
                                   at regular intervals. The assurance amount is deposited in a tripartite escrow account annualised 
                                   @5% over the mine’s life and computed @ Rs 9 lakhs/ha for opencast mine and @ Rs 1.50 lakh/ha for 
                                   underground mine. An amount equal to the annual cost is to be deposited each year throughout the 
                                   mine life compounded annually. This amount is utilized for mine restoration and rehabilitation among 
                                   other mine closure activities on production of certification of mine closure activities including 
                                   restoration and rehabilitation. 
                                </p>
                                <p>
                                     Most of the mining companies utilize their own funds (other than deposited for compensatory 
                                     afforestation in CAMPA account) from revenue costs for mine restoration and rehabilitation. 
                                </p>
                                <p>
                                    In India, a dedicated fund raising foundation for the development of mining-affected regions outside 
                                    the mine site has also been established in the name of District Mineral Foundation (DMF) as per 
                                    provisions under the Section 9B of the MMDR Act, 1957. This section of the Act prescribes the 
                                    establishment of District Mineral Foundation (DMF) in any district for the interest and benefit of persons, and areas, affected by mining related operations. The sub-section (5) and (6) of Section 9B of the MMDR Act has been amended for clarifying the rates of DMF to be paid by different categories of mines and as per MMDR Amendment Act 2015, provisions the funds for DMF will be met from additional contributions of 30% of royalty by existing miners and 10% by miners granted mines w.e.f. 12th January 2015. Ministry of Mines vide its order dated 23.04.2021 under Section 20A of the MMDR Act, 1957 has prohibited transfer of District Mineral Foundation funds to the State exchequer or State level fund (by whatever name called) or Chief Minister’s Relief Fund or any other funds or schemes. The DMF funds are sanctioned for projects under various programmes/development schemes of Pradhan Mantri Khanij Kshetra Kalyan Yojana (PMKKKY). 
                                </p>
                                <p>
                                    The state also collects additional money from mining lease holders based on the mineral value and 
                                    associated sales. However, there is a need to generate funds for old, abandoned mines. 
                                </P>
