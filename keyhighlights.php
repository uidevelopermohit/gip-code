                                sustainable land management by actively contributing to the operational development of the Global 
                                Initiative. We are committed to further meaningful and inclusive engagement with the whole of society, 
                                including engaging private sector and civil society, Indigenous peoples, local communities, women, 
                                youth and children, persons with disabilities and people in vulnerable situation, academia, governments 
