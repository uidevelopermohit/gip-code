                            <h5 class="heading_line">Leaders’ Declaration (Italy – 2021)</h5>
                            <p><b>Environment</b></p>
                            <p><i class="icon-right-arrow"></i>&nbsp;We commit to strengthen actions to halt and reverse biodiversity loss 
                            by 2030 and call on CBD Parties to adopt an ambitious, balanced, practical, effective, robust and 
                            transformative post 2020 Global Biodiversity Framework at COP15 in Kunming. We welcome the launch of the 
                            UN Decade on Ecosystem Restoration 2021-2030, reaffirm the shared ambition to achieve a 50% reduction of 
                            degraded land by 2040 on a voluntary basis, and will strive to achieve Land Degradation Neutrality by 2030. 
                            We will build on the G20 Global Initiative on Reducing Land Degradation and Enhancing Conservation of 
                            Terrestrial Habitats launched under Saudi Presidency and look forward to its upcoming Implementation 
                            Strategy. We recognize the efforts made by a number of countries to adhere to the Leaders’ Pledge for 
                            Nature and to ensure that at least 30 % of global land and at least 30 % of the global ocean and seas are 
                            conserved or protected by 2030, and we will help to make progress towards this objective in accordance with 
                            national circumstances. We encourage and support others to make similarly ambitious commitments. We will 
                            pursue our efforts to ensure the conservation, protection and sustainable use of natural resources and will 
                            take concrete measures to end overfishing, deliver on our commitment to end illegal, unreported and unregulated 
                            fishing, and combat crimes that affect the environment such as illegal logging, illegal mining, illegal 
                            wildlife trade and illegal movement and disposal of waste and hazardous substances. We underline the many 
                            synergies in financial flows for climate, biodiversity and ecosystems, and we will strengthen those synergies 
                            to maximize co-benefits. In this context, we recognize the importance of work on nature-related financial 
                            disclosure.
                            </p>
                            <p><i class="icon-right-arrow"></i>&nbsp;
                                Acknowledging the urgency of combating land degradation and creating new carbon sinks, we share the 
                                aspirational goal to collectively plant 1 trillion trees, focusing on the most degraded ecosystems in 
                                the planet, and urge other countries to join forces with the G20 to reach this global goal by 2030, 
                                including through climate projects, with the involvement of the private sector and civil society.
                            </p>
