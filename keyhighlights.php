                            <p><b>Land Degradation and Habitat Loss</b></p>
                            <p><i class="icon-right-arrow"></i>&nbsp;We stress that ecosystems including, but not limited to, forests, grasslands, drylands, rangelands, 
                                croplands, peatlands, mangroves, soil, tundra, karst, and wetlands are essential for the existence,
                                survival and sustainability of biodiversity as well as food security and human well-being and are an 
                                example of natural capital. We emphasize that healthy ecosystems provide vital provisioning and regulating
                                services including maintaining air quality and freshwater resources, supplying food, medicinal products, 
                                wood and fiber, mitigating pollutants, supporting human health and well-being as well as nature-based 
                                recreation. In addition, healthy ecosystems play an important role in absorbing emissions and in reducing
                                the risk of natural disasters and adapting to extreme weather events as well as in limiting land 
                                degradation, including by combating desertification and mitigating against future diseases and pandemics.
                                In this sense, it is important to promote public programs and policies for the development of payment 
                                for ecosystem services taking into account the economic valuation of ecosystems and their services to
                                inform decision-making as well as develop public private partnerships and provide incentives for
                                private sector and stakeholder engagement and for the creation of a functional market for such 
                                services. We also acknowledge that land degradation in all its forms is a key force exacerbating
                                biodiversity loss by driving fragmentation and loss of terrestrial habitats. Moreover, land 
                                degradation threatens a significant and increasing number of habitats and species around the world.
                            </p>
                            <p><i class="icon-right-arrow"></i>&nbsp;
                                We recognize the urgent need for bold, coordinated, and collective initiatives on land protection and 
                                restoration and on sustainable land management and use to strengthen existing efforts, such as the Bonn 
                                Challenge, the United Nations Decade on Ecosystem Restoration, and other multilateral initiatives, as well 
                                as to promote new ones. We recognize the benefits of cooperation between neighboring countries in 
                                sustainable land and water management and ecological conservation and restoration. We acknowledge the 
                                benefits of sustainable agriculture as an approach that adopts methods and practices that pursue 
                                productive, sustainable, and resilient food production systems that minimize harmful impacts to the 
                                environment, biodiversity and food security across the system – for the current generation and the 
                                generations after. We further acknowledge the benefits of the existing efforts and commitments such
                                as those developed for Land Degradation Neutrality (LDN), Forest Landscape Restoration, Ecosystem-based 
                                Adaptation, and Disaster Risk Reduction as well as targets under the Convention of Biological Diversity and 
                                the United Nations Convention to Combat Desertification. We also recognize the contributions made through 
                                actions taken to meet the Aichi Biodiversity Targets. We call for increased efforts and cooperation to 
                                achieve Land Degradation Neutrality as set out in Sustainable Development Goal (SDG) 15.3 target, as well 
                                as to scale-up successful and appropriate action that will contribute to LDN without hampering the 
                                achievement of other SDGs. In the context of the economic recovery from COVID-19 and the associated 
                                challenges faced by our food systems and supply chains, work towards promoting sustainable value chains, 
                                delivering nature-based solutions or ecosystem-based approaches, and implementing sustainable land use 
                                measures and secure land tenure with gender responsiveness will yield direct benefits to the livelihoods 
                                of rural and indigenous communities. We encourage public and private engagement to mobilize finance and 
                                investment for effective implementation of such vital measures to maximize the potential opportunity 
                                provided by these.
                            </p>
                            <p><i class="icon-right-arrow"></i>&nbsp;
                                We stress the importance of science- and evidence-based approaches to policy making and take note of the 
                                findings of the international and regional bodies spearheading robust research and assessment activities. 
                                We also acknowledge the role of the traditional knowledge of Indigenous Peoples and local communities. We 
                                support research and development, harnessing science, technology and innovation, and leveraging technologies
                                towards implementing the 2030 Agenda for Sustainable Development as well as other related development 
                                initiatives. We note with deep concern the findings presented in recent scientific assessments, which 
                                reflect the best available science and highlight the fact that unprecedented exploitation of land and 
                                freshwater resources are implying higher risks to people’s livelihoods and well-being, and that 
                                biodiversity is declining globally at a rate unprecedented in human history, in particular due to 
                                unsustainable use of natural resources and land degradation. We also note the think-piece by the 
                                International Resource Panel on ‘Land Restoration for Achieving the Sustainable Development Goals’ 
                                according to which land restoration and rehabilitation can have significant co-benefits for all the SDGs. 
                                In the context of the evidence above, we underscore the importance of continuing work at all levels to 
                                achieve the inter-linked objectives of the United Nations Decade on Ecosystem Restoration, the three Rio 
                                Conventions and the Global Forest Goals of the United Nations Strategic Plan for Forests, guided by the 
                                2030 Agenda for Sustainable Development. We are committed to combating land degradation and habitat loss 
                                in the framework of SDG 15 and the United Nations Decade on Ecosystem Restoration, and to stepping up our 
                                efforts on the conservation and sustainable management of land and biodiversity in advance of the fifteenth 
                                meeting of the Conference of the Parties (COP15) to the CBD, the fifteenth session of the Conference of the 
                                Parties (COP 15) of the UNCCD and throughout the United Nations Decade on Ecosystem Restoration. We call 
                                for all CBD parties to adopt at CBD COP 15 an ambitious, realistic, practical, and effective Post-2020 
                                Global Biodiversity Framework with strong implementation support mechanisms, responsibility and 
                                transparency, including enhanced reporting, to facilitate the transformational changes needed to 
                                achieve the 2050 Vision for Biodiversity.
                            </p>
                            <p> <i class="icon-right-arrow"></i>&nbsp;   
                                We support the launch of a Global Initiative on Reducing Land Degradation and Enhancing Conservation of 
                                Terrestrial Habitats which aims to support existing efforts to prevent, halt, and reverse land degradation 
                                and habitat loss through sharing of knowledge and best practices on protecting, conserving, sustainably 
                                managing, restoring, and rehabilitating degraded land, and by showcasing and disseminating publicly 
                                available data and information on degraded lands and conservation/restoration efforts. The initiative will 
                                also contribute to capacity building and encourage greater private sector support and general public 
                                engagement in land restoration efforts. The initiative focuses on complementing and supporting existing
                                efforts while striving to avoid any duplication of efforts. The Initiative will seek synergies with 
                                existing relevant initiatives including the UN Decade on Ecosystem Restoration and the implementation 
                                of the post-2020 Global Biodiversity Framework.
                            </p>    
