                            <p><i class="icon-right-arrow"></i>&nbsp;
                            We welcome the successful convening of the workshop organized by the Italian Presidency on the G20 Global 
                            Initiative on Reducing Land Degradation and Enhancing Conservation of Terrestrial Habitats launched under 
                            Saudi Arabia’s Presidency in 2020. We reiterate our support for the Initiative and on a voluntary basis, 
                            will continue to lead, strengthen cooperation, further promote investment and provide adequate means of 
                            implementation and share experiences on land protection, conservation, sustainable management and restoration 
                            initiatives and programs in line with the objectives of the Initiative.
                            </p>
                            <p><i class="icon-right-arrow"></i>&nbsp;
                            We stress that the UN Decade on Ecosystem Restoration is a crucial window of opportunity to protect, conserve, 
                            restore and sustainably manage land, including forest, and water resources. Building on existing initiatives we 
                            share the ambition to achieve a 50% reduction of degraded land by 2040, on a voluntary basis. We will continue 
                            our efforts to combat desertification, restore degraded land and soil, and strive to achieve Land Degradation 
                            Neutrality by 2030 in furtherance of SDG Target 15.3 and a number of related Sustainable Development Goals and 
                            Targets. We encourage the integration of land conservation, restoration and sustainable management into post 
                            COVID19 recovery plans and strategies. We emphasize that healthy ecosystems, inter alia, forests, grasslands, 
                            drylands, rangelands, croplands, peatlands, mangroves, soil, tundra, karst, and wetlands are essential for the 
                            sustainability of biodiversity, food security and supporting human health and well-being. This underscores the 
                            importance of terrestrial ecosystems and the services they provide including for both global food security and 
                            resilience to future shocks. In this context, we welcome the G20 Matera Declaration on Food Security, Nutrition 
                            and Food Systems, contributing as appropriate to working with relevant Ministers towards the UN Food Systems 
                            Summit. We support voluntary global and national efforts to protect and conserve land and ecosystems in 
                            accordance with national priorities and policies. We aim to coordinate our actions with ongoing multilateral 
                            efforts for the protection conservation, sustainable management and restoration of degraded terrestrial 
                            ecosystems and habitats.
                            </p>
