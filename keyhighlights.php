                            <h5 class="heading_line">Leaders’ Declaration (Indonesia – 2022)</h5>
                            <p><b>Environment</b></p>
                            <p><i class="icon-right-arrow"></i>&nbsp;
                                We welcome the progress to date towards achieving a Post 2020 Global Biodiversity Framework (GBF). We urge 
                                all parties and countries to finalize and adopt the GBF with the view of realizing of 2050 Vision of 
                                “Living in harmony with Nature” at the second part of COP15 CBD as a strong framework of action and 
                                accountability for halting and reversing biodiversity loss by 2030 and, as appropriate, to update National 
                                Biodiversity Strategies and Action Plans accordingly. We emphasize the importance of achieving and 
                                synergizing the objectives of the three Rio Conventions. We stress the need for clear and measurable 
                                goals and targets for biodiversity and means of implementation and accountability. We commit to strengthen 
                                actions to halt and reverse biodiversity loss by 2030 and call on CBD Parties to adopt an ambitious, 
                                balanced, practical, effective, robust and transformative post-2020 Global Biodiversity Framework at 
                                COP-15 in Montreal. We urge for increased resource mobilization from all sources, including from 
                                countries and entities, to provide new and additional financial resources for the implementation of the 
                                GBF, once it is negotiated, including to help enable and support developing country parties, and for 
                                aligning private and public financial flows with biodiversity objectives. We will scale up efforts to 
                                combat biodiversity loss, deforestation, desertification, land degradation and drought, as well as 
                                restoring degraded land to achieve land degradation neutrality by 2030, and in support of the G20’s 
                                ambition to reduce land degradation by 50% by 2040 on a voluntary basis. We recognize the effort made by a 
                                number of countries to ensure that at least 30% of global land and at least 30% of the global ocean and 
                                seas are conserved or protected by 2030 and we will help to make progress towards this objective in 
                                accordance with national circumstances. We commit to reduce environmental impacts by changing unsustainable 
                                consumption and production patterns as well as to enhance environmentally sound waste management including 
                                by preventing illegal cross-border traffic of waste.
                            </p>
                            <p><i class="icon-right-arrow"></i>&nbsp;
                                We will step up efforts to halt and reverse biodiversity loss, including through Nature-based Solutions 
                                and Ecosystem-based Approaches, support climate mitigation and adaptation, enhance environmental 
                                conservation and protection, sustainable use and restoration, responding to natural disasters, reduce 
                                ecosystem degradation, enhance ecosystem services and to address issues affecting the marine and 
                                coastal environment. We will further promote sustainable development and lifestyles, resource 
                                efficiency and circular economy to increase sustainability and work together on scientific 
                                knowledge-sharing, raising awareness, and capacity building, particularly to advance on the 
                                ocean-based climate action. We are committed to ending illegal, unreported and unregulated 
                                fishing. We welcome the WTO multilateral Agreement on Fisheries Subsidies and encourage its 
                                rapid entry into force. In line with the UNEA Resolution 5/14, we are committed to develop an 
                                international legally binding instrument on plastic pollution, including in the marine environment,
                                with the ambition of completing the work by the end of 2024. We highlight the progress made and call on 
                                participating delegations to achieve an ambitious and balanced agreement without delay on an 
                                international legally binding instrument under UNCLOS on the conservation and sustainable use of marine 
                                biological diversity of areas beyond national jurisdiction, as called for in the UNGA Resolution 
                                69/292. We also acknowledge that ecosystems, including forests, seagrasses, coral reefs, wetland 
                                ecosystems in all their diversity, including peatlands and mangrove, support climate change mitigation 
                                and adaptation efforts.
                            </p>
