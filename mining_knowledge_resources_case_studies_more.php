                                            <strong> b.	Mine Reclamation: A handbook, Indian council of Forestry and Research Education </STRONG></br>
                                                Minerals and mining sector contributes significantly to the economic growth and development of India in the form of exports, raw material to other industries in the country and finally vast employment opportunities and a high rate of wages. The Indian mining industry currently employs over 1.1 million people. Mining also yields foreign exchange and accounts for a significant portion of gross domestic product. Thus, the economic opportunities and wealth generated by mining for the country are substantial. However, mining is associated with severely adverse effects on the environment including loss of biodiversity, erosion, contamination of surface water, ground water, and soil. Mining causes physical destruction to the surrounding land by creating landscape blots such as open pits and piles of waste rock. Such disruptions result in the deterioration of the area's flora and fauna. In most of the cases the surface features and vegetation that were present before mining activities cannot be replaced after the process has ended. Loss of soil productivity, biological diversity, health and environmental hazards due to air, water and noise pollution microclimatic changes etc. are the major problems of the mining process. Reclamation on mined areas thus pose a great challenge for ecologists, biologists, agriculturists, engineers, soil scientist, and foresters as well who are invariably concerned about restoration of this disturbed ecosystem. Therefore, Indian Council of Forestry Research and Education has come up this operational manual incorporating comprehensive information on various aspects of mine reclamation including extent and causes of land degradation, scope and reclamation scenario in India, approaches and strategies of reclamation of mine spoils, essentials of ecofriendly mining and species prescriptions for different mining scenarios. The manual also contains outstanding reclamation efforts of various ICFRE institutions in recent past. 
