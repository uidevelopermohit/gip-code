                                            <STRONG>c.	An Ecological Appraisal of Reclaimed Limestone Mines in Mussoorie Hills, India (The Indian Forester, International Journal)</STRONG></br>
                                            Mining is the major problem causing land degradation and affects all types of land use and the fragile ecosystem. Surface mining activities inevitably cause massive damage to the land environment (Bandyopadhyay and Maiti, 2019). Total removal of vegetation cover and topsoil, and destruction of surface and sub-surface hydrologic regimes are among the deleterious effects. Massive alterations in the landscape include overburdened dumps consisting of large boulders and loose rock fragments that are devoid of moisture and nutrients. However, mining is considered a temporary land use where productivity may be restored through proper reclamation work. Vegetation has a significant role in protecting the soil surface from erosion and permitting the accumulation of fine particles (Tordoff et al., 2000; Conesa et al., 2007). This can reverse degradation by stabilizing soil conditions. Plants increase the organic matter, lower the bulk density and moderate the soil pH. The diversity of climate, rainfall, altitude, and soil conditions support different plants and animal communities. Plants should be selected based on their ability to survive, regenerate, and reproduce under severe conditions (Madejon et al., 2006). Thus, the role of exotic and native species in reclamation needs careful consideration. Native leguminous species show more significant improvement in soil fertility than native non-leguminous species (Singh et al., 2002). Analysis of the overburden helps in suggesting the mine spoil characteristics, and a better and more effective selection of multipurpose suitable plant species for restoration.  
