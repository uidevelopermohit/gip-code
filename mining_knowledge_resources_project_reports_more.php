                                Almost all the mining techniques from mineral explorations to production and transport are causing environmental damage in several ways. The list includes deforestation, loss of topsoil, accelerated soil erosion, soil contamination, qualitative and quantitative depletion of surface and ground water resources, migration of wildlife and avian fauna, and addition of air pollutants and dust in the atmosphere. Land degradation, disruption in the goods and services offered by the nature and forest has been compromised greatly. In lieu of this, to reverse the degradation of environment in Dhanbad coalfield, a model plantation/ecological restoration was carried with the objectives i) To stabilize overburden dumps to control erosion; ii) To conserve and enhance the biodiversity of mined out areas; iii) To generate natural resources to cater the needs of local people and iv) To develop aesthetic view of mined out area. Based on the condition of area, different ecological restoration approaches were applied during project period of three years (2011- 2014). A total of 13,000 plants of different species including horticulture species have been planted in the project site.
