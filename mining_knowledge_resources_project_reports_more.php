                                <STRONG>b. Road Map for Ecological Restoration Works in Coal Mines of NCL, Singrauli, M.P.; Forest Research Institute, Dehradun, India </STRONG></br>
                                The various activities required for ecological restoration of coal mines have been discussed and suggested for their implementation in the field as the part of road map. These activities included the management of waste dumps through application of engineering measures such as toe/retention walls, garland drains, geo-textile/coir mat/jute mat, and measures for the surface water management such as gabion check dam, loose boulder check dam, stone masonry check dam, log wood check dam, brush wood check dam, silt settling tank, etc., and techniques for soil and moisture conservation (i.e., mulching, top soil spreading, raising grasses, etc.). As a part of biological measures this included selection of species, methods of planting (Direct seed sowing, seed mix soil ball/slurry, hydro seeding, seedling planting), etc. Measures for the management of backfilled OB dump, haul and approach roads have also been suggested in road map. Native species suitable for avenue plantation and wastelands, green belt development, socioeconomic development and livelyhood opportunities have been suggested. Suitable leguminous and grass species for use in restoration are also suggested separately.</p>
                               
