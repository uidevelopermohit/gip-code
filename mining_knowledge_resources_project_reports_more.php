                                <STRONG>c.	Enhancement Of Biodiversity And Its Conservation Ion The Ecological Restoration Site At Tetulmari, Sijua Area Of BCCL Dhanbad (Forest Research Institute, ICFRE, India)</STRONG></br>
                                Ecological restoration has the potential to reverse the land degradation, increase the resilience of biodiversity, and deliver important ecosystem services that facilitate the recovery of structural and functional components of ecosystem, which have been destroyed or degraded by mining activities. Coal is a most important and abundant fossil fuel having a proven reserve of 860 billion tones, out of which 55% of it is being utilized in meeting country’s energy demand. The power sector followed by iron, steel, and cement segments are the largest consumer of coal in India. Thus, it becomes inevitable for continuous mining of coal. Coal mining whether it is done by strip mining (open cast mining) or underground mining, are equally responsible for environmental degradation.  </p>
                                <p>Ecological restoration followed by enhancement of biodiversity in the ecologically restored site was carried out in 8ha coal mined OB dumps at tetulmari, Sijua area of BCCL, Dhanbad. Nearly 6060 plant of different species of multiple uses were planted by various means such as sowing, seedling, planting, stem cutting at the site. Out of them, there were 24 tree species, 2 shrubs, 2 grasses and 1 bamboo species. Species planted at the site were assessed for their growth. Soil samples from the site were collected and analysed for their physio-chemical properties. Vegetation analysis in 2016 and 2017 was carried out using quadrat sampling method. The quadrats size were 5m X 5m and 1m X 1m respectively to record trees, shrubs, saplings and herbaceous layer. </p>
