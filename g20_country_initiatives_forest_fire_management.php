                            <h5>Forest Fire Management in Yellapurra Division, Karnataka.</h5>
                            <p>
                                     One of the main forest regions in the Western Ghats is Yellapura, which is located in the Karnataka 
                                     district of Uttara Kannada. Uttara Kannada being the only district in Karnataka that is 80 percent 
                                     forested and is located in the Western Ghats. The Yellapura taluk has the most forest cover, with 
                                     87% of the total land being covered by trees. More than 70% of the forest's land is made up of dry 
                                     deciduous woods, which are divided into 3 subdivisions and 6 ranges. Widespread grown teak is 
                                     extremely susceptible to forest ground fires. In the drier months, forest fires can happen in any 
                                     forest, and they play a significant role in influencing the vegetation and terrain.
                                </p>
                                <p>
                                    The Forest Department of the region initiated a community-based approach to prevent and control the 
                                    incidences of Forest Fires in the region. The measures taken were a mixture of the community 
                                    involvement and Capacity Building approach. Some of them have been listed as below:
                                </p>
                                <p>
                                        <b>1.</b>Awareness Programmes: Awareness programmes were initiated at schools at division level to make 
                                        students aware about the harms and preventive measures for forest fire, the schools were also 
                                        encouraged to report any such incidents. Posters were deployed at community places where the 
                                        general public can sight them easily and be aware of the Dos and DON’Ts for the forest fire 
                                        incidents. 
                                </p>
                                <p>
                                        <b>2.</b>Local Authorities Meetings: Meetings of all 111 Village Forest Communities (VFCs) of Yellapura 
                                        Forest division were conducted to raise awareness. Along with these, leaders of Gram Panchayats 
                                        were also taken into confidence for the purpose of creating awareness.
                                </p>
                                <p>
                                        <b>3.</b>Beedhi Nataks: Street Plays were conducted in almost all the divisions to sensitize the general 
                                        public    
                                </p>
                                <p>
                                    <b>4.</b>Fire Control Room: A round-the-clock fire control centre was set up at the DCF office, and the 
                                        department's phone number was extensively disseminated throughout the nearby villages.
                                </p>
                                <p>
                                        <b>5.</b>Door to door campaign: By adopting a vow in the name of their local deity, Lord Manjunateshwara, 
                                        the Forest Department and Sri Dharmasthala Grameena Abhivruddhi Sangha developed pamphlets to 
                                        discourage communities from engaging in any criminal activity in the forest, such as igniting 
                                        forest fires, stealing, or poaching.
                                </p>
                                <p>
                                    Along with these, measures such as Oath- taking, building of forest fire watch towers, distribution 
                                    of approximately 5000 calenders to create a sense of community between locals and forest department 
                                    was also done, and giving away of awards to Village Forest Communities (VFCs) for actions related to 
                                    forest fire prevention were also adopted.
                                </p>
                                <h5>Results for Community awareness programmes: </h5>
                                <p>
                                    Prior to the use of community-based techniques, reports of forest fires made up around 5–7% of the 
                                    total forest area, and FSI notifications were always received in 4-digit format. Before the community
                                    awareness programme, only hired watches, labourers, and department staff were involved in the fire 
                                    extinguishing procedure, which resulted in a very lengthy reaction time for forest fires. 
                                    Additionally, a greater area was affected by forest fires. The graph shows that the number of fire 
                                    alarms in the years 2018–19 has significantly decreased since the community-based measures were put 
                                    in place. In the Yellapura division, there were 5% fewer fire alerts in 2018 than there were in 2017.
                                    In addition, compared to 0.046% in 2017–18, the area burned by fire decreased to 0.019% in 2018–19. 
                                    It was evident from the results that the burnt area as a result of forest fires in 2018–19 
                                    drastically decreased by more than 58%. This demonstrates that the response time to put out the fire 
                                    was sped up. As a result of raising local residents' awareness prior to the fire season, major fire 
                                    incidents were also reduced to an appallingly low level. Additionally, there were fewer instances of 
                                    natives starting fires. All of the aforementioned initiatives demonstrate how dramatically better 
                                    reporting, putting out, and managing forest fires have been made possible by community-based 
                                    techniques.
                                </p>
                                 
