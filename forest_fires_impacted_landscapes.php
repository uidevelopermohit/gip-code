                                <h2>Policy and Programs</h2>
                                <p>
                                     Legislation and policy at the national-level provides the overall framework and direction for 
                                     Forest Fire Prevention and Management (FFPM) in India. Although fire has been used as a land 
                                     management tool by traditional cultures in India for thousands of years (Pyne 1994), national 
                                     laws strictly forbid setting fire in forests. Sections 26 and 33 of the Indian Forest Act of 1927 
                                     make it a criminal offense to burn or to allow a fire to remain burning in reserved and protected 
                                     forests. Section 30 of the Wildlife (Protection) Act of 1972 further prohibits setting fire in 
                                     wildlife sanctuaries. These laws establish the basis for the strict exclusion of fire from India’s 
                                     forests—the one exception is controlled burning done by the forest department.
                                </p>
                            </div>
                        </div>
                        <div class="flex_row ">
                            <div class="col_12 gradient_container">
                                <h5>Major policies and programmes:</h5>
                            </div>
                            <div class="col_12"> 
                                        <p>
                                           1.&nbsp; The National Forestry Policy, issued in 1988, briefly mentions forest fires, stating in 
                                            paragraph 4.82: “The incidents of forest fire in the country is high. Standing trees and 
                                            fodder are destroyed on a large scale and natural regeneration annihilated by such fires. 
                                            Special precautions should be taken during the fire season. Improved and modern management 
                                            practices should be adopted to deal with forest fires.”
                                        </p>
                                        <p>
                                           2.&nbsp; In Madhya Pradesh, strategies for fire management that have been identified in the State 
                                            Forest Policy (2005) include developing a “new fire protection system” after a detailed 
                                            study of the effects of fire on forests in the state (including beneficial and harmful 
                                            effects), as well as using “modern techniques and equipment” to control forest fires.
                                        </p>
                                        <p>
                                           3.&nbsp; In 2016, Odisha issued Standard Operating Procedures (SOP) for fire prevention and 
                                            management, which stands out as a best-practice example. Issuing a SOP is a standard 
                                            management practice and an effective method of communicating the objectives, principles, 
                                            and actions for FFPM to field staff in the state forest departments.
                                        </p>
                                        <p>
                                           4.&nbsp; In Chhattisgarh, these FFPM guidelines include clearing fire lines, carrying out controlled 
                                            burning, ensuring NTFP collection without using fire, engaging fire watchers or JFMC members 
                                            to monitor forest areas adjacent to agricultural land and habitation, using fire watch 
                                            towers, providing adequate firefighting equipment to field staff, and keeping water tankers 
                                            ready.
                                        </p>
                                        <p>
                                           5.&nbsp; Standing instructions for FFPM in Himachal Pradesh call for: “Detailed information about the 
                                            causes and thorough understanding of the motives behind the forest fire… provide the 
                                            background (sic) fire prevention work” (HP 2015: 72).” Special measures are provided for 
                                            fire prevention in chir pine forests and controlled burning in forests not under 
                                            regeneration. Specifications for fire detection through observatory towers and 
                                            instructions on suppressing fire are also provided (HP 2015: 72-76).
                                        </p>
                                        <p>
                                           6.&nbsp; The role of local communities in fire prevention and management has been institutionalized 
                                            through Joint Forest Management (JFM). Under national guidelines, all JFM sites must be 
                                            covered by working schemes, prepared in consultation with the community and approved by 
                                            the state forest department, which should include prescriptions for fire protection.
                                        </p>
                                        <p>
                                            7.&nbsp; The National Afforestation Programme (NAP): The NAP delivers central financing to 
                                            village-level Joint Forest Management Committees (JFMCs) for rehabilitating and 
                                            afforesting degraded lands. From its inception in 2000 to 2016, the NAP has distributed 
                                            INR 3,640 crore (US$ 541.7 million) to fund afforestation projects on over 2.1 million 
                                            hectares. The NAP is a 100-percent centrally funded scheme.
                                        </p>
                                        <p>
                                           8.&nbsp; The Mission for Green India (GIM): Approved in 2014, the GIM was established with the goal 
                                            of realizing the government’s target to increase forest cover by 50,000 km2 and to improve 
                                            the quality of forest on another 50,000 km. As of mid-2017, the GIM was being implemented in 
                                            13 states and had released a total of INR 113 crore (US$ 16.9 million) for tree planting 
                                            activities on 30,000 hectares.
                                        </p>
                                        <p>
                                           9. &nbsp;The Forest Fire Prevention and Management Scheme (FPM): The FPM is the only centrally 
                                            funded program specifically dedicated to assist the states in dealing with forest fires. 
                                            The FPM replaced the Intensification of Forest Management Scheme (IFMS) in December 2017 
                                            (MoEFCC Doc. F. No. 3-1/2017-FPD, 6 December 2017). About INR 52 crore (US$ 7.7 million) 
                                            in IFMS funds were released to the states in the 2015-16 fiscal year, a third of which went 
                                            to fire prevention and management (MoEFCC 2017). By revamping the IFMS, the FPM has 
                                            increased the amount dedicated for forest fire work. For the 2017-18 fiscal year, INR 
                                            49.4 crore (US$ 7.4 million) has been allocated under the scheme, with the maintenance 
                                            of 70,000 km of fire lines and construction of 60 field crew stations expected as outputs 
                                            of this support (DEA 2017: 47).
                                        </p>
                                        <p>
                                           10.&nbsp; Compensatory Afforestation Fund Management and Planning Authority (CAMPA). An ad-hoc CAMPA 
                                            was created following a 2006 ruling by Supreme Court which ordered that compensation paid 
                                            to the state governments by users diverting forest lands (e.g., for mining, infrastructure 
                                            building, and other projects) be transferred to the fund. The payments included funding for 
                                            afforestation on non-forest land or degraded forest areas, compensation for the value of 
                                            forgone ecological services, money for watershed protection in catchments where dams are 
                                            built (Kohli et al. 2011).
                                        </p>
