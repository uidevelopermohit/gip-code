                                <p>
                                    <b>1. &nbsp;Seed Ball Technology for Restoration of Coal Mined Land, Rain Forest Research Institute, 
                                    </b>India Seed 
                                    ball technology is an old age technique adopted for propagating plants from seeds throwing seeds 
                                    balls without opening up soil with cultivation tools. Seed balls (earth balls) are made by mixing 
                                    seeds of different plant species with clay soil in the shape of balls. The seed balls were first 
                                    deployed in ancient Egypt, where they were used to restore farmlands after the Nile‟s annual spring 
                                    flood. The practice was incorporated into „guerrilla gardening‟ in the 1970s, and the movement 
                                    sought to rehabilitate derelict or neglected urban land in the USA. The popularization of this 
                                    technique in modern times happened due to Masanobu Fukuoka who introduced natural farming called 
                                    "the Fukuoka Method", wherein he used volcanic red clay to make seed balls which he called as 
                                    “Clay Dumplings”. Fukuoka‟s natural farming minimizes human labour adopting the principle of no 
                                    tillage, no fertilizer, no pesticides, no weeding, no pruning and as closely as possible to natural 
                                    succession. The seeds germinate on the soil surface when the site conditions meet the needs of the 
                                    seeds. Later, with time modification has been made on seed ball preparation incorporating various 
                                    additives such as humus or compost, microbial inoculants, cotton fibres, liquefied paper mash, etc., 
                                    to protect the seed balls in harsh habitats and to promote the germination and growth.
                                </p>
                                <p>
                                   <b>2. &nbsp; Plantation technology for restoration of coal mined land (The Indian Forester, International Journal)</b>
                                    Plantation technology focuses on restoring coal-mined land by establishing a diverse and sustainable 
                                    ecosystem. The process begins with the establishment of a suitable grass cover on the reclaimed land. Once this initial step is successful, the subsequent plantation of native tree species is undertaken over the next couple of rainy seasons. This holistic approach to land restoration not only helps in stabilizing the ecosystem but also enhances soil health and biodiversity. By utilizing native plant species and beneficial microorganisms, this technology aims to accelerate the reclamation of coal-mined land and promote ecological sustainability.
                                </p>
