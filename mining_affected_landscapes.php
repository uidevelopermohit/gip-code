                                 <h2>Policy and Programs</h2>
                                 <h5>Statutory Framework and Provisions for Mine Restoration and Rehabilitation</h5>
                                 <p>
                                    1.&nbsp;  The National Mineral Policy 2019 mandates the restoration and rehabilitation of mine sites by 
                                    promoting scientific: mine closure and the regeneration of biodiversity. The government plays a 
                                    critical role in ensuring mine decommissioning and land reclamation. The mining industry should 
                                    prioritize financial provisions for the costs associated with mine closure.
                                </p>
                                <p>
                                    2.&nbsp;  Chapter 7 of the Mines and Minerals (Development and Regulation) Act, 1957 grants the Central 
                                    Government the power to issue directions for restoration and reclamation activities.
                                </p>
                                <p>
                                    3.&nbsp;  The Coal Mines {Conservation & Safety) Act, 1974, focuses on enhancing coal efficiency, promoting 
                                    ecofriendly technology, and conserving coal resources.
                                </p>
                                <p>        
                                    4.&nbsp;  Several acts and rules, including The Water (Prevention and Control of Pollution) Act, 1974; The 
                                    Water (Prevention and Control of Pollution) Cess Act, 1977, The Air {Prevention and Control of 
                                    Pollution) Act, 1981. The Environment Protection Act (EPA), 1986 (amended in 2006) and the 
                                    Forest (Conservation) Act, manage the environment and protect it from mining activities.
                                </p> 
                                <p>
                                    5.&nbsp;  The National Environmental Policy, 2006, aims to conserve environmental resources by promoting 
                                    resource efficiency, intergenerational equity, good environmental governance, and environmental 
                                    protection.
                                </p>
                                <p>
                                    6.&nbsp;  The Indian Bureau of Mines (IBM) oversees mining administration in India.
                                </p>
                                <p>               
                                    7.&nbsp;  Ministry of Environment, Forest and Climate Change (MoEFCC is responsible for granting environmental 
                                    clearance for all mines In India. State and District Environment Impact Assessment Authorities may 
                                    also grant environmental clearance for mining leases up to a certain area.
                                </p>
                                <p>
                                    8.&nbsp;  The Coal Controller Organization {CCO) and the MoEFCC regulate and monitor mine closure activities.
                                </p>
                                <p>
                                    9.&nbsp;  Directorate General of Mines Safety (DGMS) enforces provisions related to labour welfare and health 
                                    safety in mines under the Occupational Safety, Health and Working Conditions (OSH) Code 2020 and 
                                    subordinate legislation like Coal Mines Regulation 1957, Metalliferous Mines Regulation 1961, Mines 
                                    Vocational Rules 1966, Oil Mines Regulations 1984, Mines Rescue Rules 1985, and Mines Creche Rules 
