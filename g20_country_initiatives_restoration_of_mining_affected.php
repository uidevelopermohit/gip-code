                            <h5>Restoration of mining-affected land of Tikak Coillery, Margarita, Assam with Integrated Biological Approach</h5>
                                <p>
                                <strong>Brief Summary:</strong> 
                                A study was conducted In Tikak Colliery, Margherita, Assam, India, to evaluate the performance of 42 native plant species in a revegetation experiment with an integrated biological approach between 2018 and 2021. The experiment took place at a backfill area of a coal mine overburden dump (080). To prepare for the plantation, lime was applied at a rate of 15 gm per pit, and farmyard manure (FYM) was applied at a rate of 2 kg per pit. The planting of nursery-raised inoculated seedlings with arbuscular mycorrhizal fungi (AMF) and plant growth-promoting rhizobacteria (PGPR) followed a series of pot experiments to determine the best treatment combination for the field trial. Seed ball technology was also used In the first year to stabilize the area with green herbaceous cover and to Improve soli parameters. The results showed that herbaceous species grown from seed ball sowing were able to survive in the OBD area, and a total of 26 naturally colonizing plant species, dominated by the Poaceae family, were recorded from the site three years after seed ball sowing. Callicarpa arborea and Schima wallichii were among the tree species, while Chromolaena odorata and Lantana camara were invasive species that naturally colonized the OBD. The best-performing treatment combination was AMF + PGPR + Lime + FYM. Seventeen out of a total of 42 native plant species achieved 60-70% survival after two years of planting on the OBD site. These practices replaced the need to apply topsoil In the coal mine site's revegetation program and can be replicated for the successful reclamation of OBD sites for a plantation program.
                                </p>
                                
                                <p>
                                    <b>Lessons Learned:</b> 
                                </p>
                                <p>
                                	<b>1. Use of Integrated Biological Approach:</b> The integrated biological approach, which involves the use of soil amendments, plant growth-promoting rhizobacteria, and arbuscular mycorrhizal fungi, proved to be effective In restoring degraded land. This approach can be useful for other restoration projects.
                                </p>
                                <p>
                                    <b>2. Seed Ball Technology:</b> The use of seed ball technology, which involves the preparation of seed balls mixed with clay soil and organic manure, proved to be a low-cost and eco-friendly technique for propagating native plant species in the degraded site. This technique can be useful in other restoration projects, particularly In areas where traditional cultivation methods are not feasible.
                                </p>
                                <p>
                                    <b>3. Site Stabilization:</b> The use of physical earthwork techniques such as bench dumping, contouring, and planting grass species and rhizome of Melocanna baccifera helped to stabilize the site, reduce soil erosion, and increase the seepage of rainwater. This can be useful In other restoration projects where site stabilization is critical.
                                </p>
                                <p>
                                    <b>4. Community Involvement:</b> The involvement of local communities in the restoration project was crucial for its success. The project provided employment opportunities for the local people and also helped to raise awareness about the Importance of conservation and restoration of degraded ecosystems.
                                </p>
                                  
