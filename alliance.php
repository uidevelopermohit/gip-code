                    </p>


                    <h5>Proposed Structure</h5>
                    <p>The alliance will be governed by a 3-tier governance structure. </p>
                   
                     <p>
                     <i class="icon-right-arrow"></i>&nbsp; Alliance Coordination Committee – The apex governing body of the 
                     alliance with a nominated HoD level officer from all countries with the mandate to represent the alliance 
                     internationally, approve addition of new members, partners & landscapes and guide the secretariat in implementing
                      alliance activities.
                    </p>
                    <p>
                    <i class="icon-right-arrow"></i>&nbsp;Expert Working Group – Senior level expert committee nominated by members of 
                    the alliance coordination committee with 1 member on each priority landscape from each country tasked with identifying and technically supporting lighthouse projects as well as finalizing the annual restoration reports of the alliance.
                     </p>
                     <p>
                     <i class="icon-right-arrow"></i>&nbsp;Secretariat – Backbone of the alliance housed at SLE-COE, ICFRE with a 5-7 
