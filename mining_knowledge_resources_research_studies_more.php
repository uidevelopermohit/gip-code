                                <STRONG>c. The Role of Input Litter Quality and Quantity on Soil Organic Matter Formation and Sequestration in Rehabilitated Mine Soil (The Indian Forester, International Journal)</STRONG></br>
                                Mineral resource extraction causes enormous soil damage, altering microbial ecosystems and impacting plants, ultimately destroying vast swaths of land. Reclamation is a process of rehabilitating degraded mining land sites to restore their ecological integrity. It encompasses the management of all physical, chemical, and biological properties of soils, such as pH, fertility, microbial community composition, and various soil nutrient cycles, that contribute to the productiveness of degraded land soils. Physical disruption to topsoil during stripping, stockpiling, and reinstatement results in substantial nutrient transformations and movements, resulting in the loss of large amounts of nutrients (Singh and Seema, 2017). Revegetation is by far the most widely accepted and effective method of preventing erosion and protecting soils from degradation during reclamation (Singh et al., 2015). Surface coal mining inevitably deforests land, reduces carbon (C) pool and generates different land covers. Degraded mine soils can act as a sink for atmospheric carbon dioxide, as SOC is fundamental for various soil processes. SOC contributes towards nutrients and energy for soil microbes and plays a pivotal role in nutrient cycling. With an increase in the year of vegetation restoration, trees help sequester more atmospheric CO2 generating a self-sustaining ecosystem in forested mine soils. Organic matter functions in the soil to improve its physical, chemical, and biological properties and is essential; hence a critical level must be present to improve soil functioning. However, not all tree species have equal potential for soil carbon sequestration. Individual plant species can affect ecosystem processes and influence nutrient dynamics and carbon sequestration by various mechanisms. Organic carbon sequestration depends upon the quality, quantity of litter, and rate of decomposition. Therefore, the choice of plantation species is likely to influence both the rate and the trajectory of carbon sequestration. 
                            </p>
