                                <STRONG>b. Reclamation of Coal Mined Land with Integrated Biological Approach (The Indian Forester, International Journal) </STRONG></br>
                                In tropical countries coal is deposited underneath the forests (Bebbingtona et al., 2018). The coal mining activities severely disturb the land by removing vegetation; alter the topography, consequent dumping of subsoil also caused loss of biodiversity of the area (Haigh, 1992; Mukhopadhyay et al., 2013). Natural invasion of plant species in such are difficult and also takes time (Hazarika et al., 2006). Moreover, revegetation of such area does not succeed through normal plantation techniques (Skousen and Zipper, 2010).  The study illustrated the advantages of the plantation technology as it does not require topsoil application and application of chemical fertilizers on the plantation site. 
                                </p>
                                <p>
                                The study demonstrated the seedling survival rate of plant species between 60% to 70%. The study also provided an alternative which suggests that 5 kg natural forest topsoil and 2 kg FYM may be applied in each plantation pit at the time of plantation of seedlings raised in 5 kg poly bags without inoculating with AMF and The plantation programme in post coalmine sites can be undertaken with the best treatment combination obtained from this study i.e., planting nursery raised seedlings (1:1 ratio of soil and FYM media filled in 5kg polybags) inoculated with arbuscular mycorrhizal fungi (AMF) and plant growth promoting rhizobacteria (PGPR) and application of lime @ 15 gm per pit and farm yard manure (FYM) @ 2 kg per pit at the time of planting. 
