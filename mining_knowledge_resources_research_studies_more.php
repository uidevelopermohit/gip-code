                                <STRONG>a. Eco Friendly Approaches for Rehabilitation of Mine Spoils, Institute of Forest Genetics and Tree Breeding </STRONG></br>
After Forest conservation Act 1980 nearly 1.50 was diverted for mining alone. Other areas for which forest lands were diverted include transmission lines, hydro projects, defence and roads etc. All these areas including mine dumps need restoration and rehabilitation treatments. Mining for minerals and coal certainly destroy the eco system but it is an inevitable as a part of civilization. The increasing human need for mineral and metal resources will affect further degradation of natural habitats, as most of the mining areas are previously forests. Moreover, ecological problems found at mining sites where pose health hazards to nearby residents causing high incidence of asthma, silicosis and stomach ailments due to the mining dust and contamination of water. Mining also has led to discharge of millions of tons of waste rock called mine spoils which are dumped over millions of hectares as wastelands. Rehabilitation of these mine spoils is very essential to overcome the health hazards as well as ecological problems. But rehabilitation of mine spoils has been difficult because it has low water – holding capacity, lacks organic matter and deficient in major nutrients Nitrogen and Phosphorus. The rehabilitation of ecological restoration is the only answer for ensuring sustainable green development of mining areas and thus rehabilitation, reclamation and restoration are all continuum process to bring out the pre - disturbance stage of eco system. Further, once the degraded eco system is restored the economic value of the restored area will increase in terms of ground water, chemical alternation of pollution and fixation of CO2 and species survival (Harris and Diggelen, 2006). There are many success stores and models in the country where degraded sites have been fully restored with technical soil in puts and sites kept out of anthropocentric influences. Hence, restoration of degraded forests like mine spoils is essential for environment and livelihood.

